
public class Estacao_Ferroviaria {
	String sigla;
	String descricao;
	Linha_Ferroviaria linha_ferroviaria [];
	
	Estacao_Ferroviaria(Linha_Ferroviaria linha_ferroviaria[]){
		this.linha_ferroviaria=new Linha_Ferroviaria[linha_ferroviaria.length];
		for(int i=0;i<linha_ferroviaria.length;i++) {
			this.linha_ferroviaria[i]=linha_ferroviaria[i];
		}
	}
}
