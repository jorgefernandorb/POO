
public class Vagao {
	int nSerie;
	float capCArga;
	float compTesteira;
	float compEngate;
	String tipo;
	public Vagao() {
	}
	public Vagao(int nSerie, float capCArga, float compTesteira, float compEngate, String tipo) {
		this.nSerie = nSerie;
		this.capCArga = capCArga;
		this.compTesteira = compTesteira;
		this.compEngate = compEngate;
		this.tipo = tipo;
	}
	
}
